function duoStage(
  janeMood: PortraitMood,
  rochesterMood: PortraitMood,
  active: CharacterId | "none",
): DuoStageState {
  return {
    mode: "duo-stage",
    left: {
      character: "jane",
      mood: janeMood,
      light: active === "jane" ? "bright" : "dim",
    },
    right: {
      character: "rochester",
      mood: rochesterMood,
      light: active === "rochester" ? "bright" : "dim",
    },
  };
}

